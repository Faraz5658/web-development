//Pre increment/decrement
let x = 10;
let y = 4;

console.log(++x);    // 11
console.log(--y);    // 3
console.log(x);      // 11
console.log(y);      // 3

//Post increment/decrement
let a = 10;
let b = 4;
console.log(a++);      // 10
console.log(b--);      // 4

console.log(a);        // 11
console.log(b);        // 3